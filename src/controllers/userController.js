import { validateAddUserData, validateAutoGeneratedUserData, validateExcelData, validateParentStudentLinkData } from "../validations/userValidation.js";
import prisma from "../utils/prisma.js";
import { deleteFromCloudinary, uploadBufferToCloudinary } from "../utils/cloudinaryUpload.js";

const buildDomainFromSubdomain = (subdomain) => {
  const sanitized = String(subdomain || "")
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9-]/g, "");

  if (!sanitized) {
    return null;
  }

  return `${sanitized}.com`;
};

export const generateUsersFromExcel = async (req, res) => {
  let uploadedFile = null;

  try {
    if (!req.file) {
      return res.status(400).json({
        message: "Please upload an Excel file",
      });
    }

    uploadedFile = await uploadBufferToCloudinary(req.file.buffer, {
      folder: "learnova/users/excel",
      resource_type: "raw",
    });

    const data = readExcelFile(req.file.buffer);

    if (!data.length) {
      return res.status(400).json({
        message: "Excel file is empty",
      });
    }

    const organizationId = req.user?.id;

    const { errors, validatedRows } = validateExcelData(data, {
      organizationRole: req.user?.role,
    });

    if (errors.length > 0) {
      await deleteFromCloudinary(uploadedFile.public_id, uploadedFile.resource_type);

      return res.status(422).json({
        message: "Excel validation failed",
        errors,
      });
    }

    // Keep orgId server-side from token and allow optional email/password from Excel.
    const rowsWithOrg = validatedRows.map(({ name, role, age, gender, address, email, password, dob, work, specialization, bio, parentNationalId }) =>
      role === 'STUDENT' || role === 'TEACHER'
        ? { name, role, age, gender, address, email, password, dob, work, specialization, bio, parentNationalId, orgId: organizationId, orgRole: req.user?.role }
        : { name, role, age, gender, address, email, password, dob, work, specialization, bio, parentNationalId }
    );
    const organization = await prisma.organization.findUnique({
      where: { id: organizationId },
      select: { subdomain: true },
    });

    if (!organization) {
      return res.status(404).json({
        message: "Organization not found",
      });
    }

    const domain = buildDomainFromSubdomain(organization.subdomain);

    if (!domain) {
      return res.status(400).json({
        message: "Unable to derive a valid domain from organization subdomain",
      });
    }

    const { createdUsers, createdParents = [], skippedUsers } = await generateUsers(rowsWithOrg, domain);

    return res.status(201).json({
      message: "Users import completed",
      summary: {
        totalRows: validatedRows.length,
        created: createdUsers.length,
        skipped: skippedUsers.length,
      },
      file: {
        url: uploadedFile.url,
        public_id: uploadedFile.public_id,
      },
      users: createdUsers,
      autoCreatedParents: createdParents,
      skipped: skippedUsers,
    });
  } catch (error) {
    console.error(error);

    if (uploadedFile?.public_id) {
      try {
        await deleteFromCloudinary(uploadedFile.public_id, uploadedFile.resource_type);
      } catch (cleanupError) {
        console.error("Cloudinary cleanup failed:", cleanupError.message);
      }
    }

    if (error.message === "Cloudinary environment variables are missing") {
      return res.status(500).json({
        message: "Cloudinary is not configured correctly",
      });
    }

    return res.status(500).json({
      message: "Internal server error",
      error: error.message,
    });
  }
};



