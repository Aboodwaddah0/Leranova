export const createUserWithGeneratedCredentialsController = async (req, res) => {
  try {
    const bodyWithOrg = { ...req.body, orgId: req.user?.id, orgRole: req.user?.role };
    const { errors, validatedData } = validateAutoGeneratedUserData(bodyWithOrg);

    if (errors.length > 0) {
      return res.status(422).json({
        message: "Validation failed",
        errors,
      });
    }

    const organization = await prisma.organization.findUnique({
      where: { id: req.user?.id },
      select: { subdomain: true },
    });

    if (!organization) {
      return res.status(404).json({
        message: "Organization not found",
      });
    }

    const domain = buildDomainFromSubdomain(organization.subdomain);

    if (!domain) {
      return res.status(400).json({
        message: "Unable to derive a valid domain from organization subdomain",
      });
    }

    const result = await addUserWithGeneratedCredentials(validatedData, domain);

    return res.status(201).json({
      message: "Add new User successfully",
      data: result.user,
      credentials: result.credentials,
      parentLinkStatus: result.parentLinkStatus,
    });
  } catch (error) {
    if (error?.code === "P2002") {
      return res.status(409).json({ message: "user email already exists" });
    }
    return res.status(500).json({ message: "Server Error", error: error.message });
  }
};

